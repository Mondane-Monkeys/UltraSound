class Point {
    double x, y, z;
  
    public Point (double inputX, double inputY, double inputZ) 
    {
      x = inputX;
      y = inputY;
      z = inputZ;
    }
    public double getX() {
      return x;
    }
    public double getY() {
      return y;
    }
    public double getZ() {
      return z;
    }
}
